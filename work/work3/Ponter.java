package com.work.work3;

public class Ponter {
    private double x;
    private double y;

    public Ponter(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public Ponter() {
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
